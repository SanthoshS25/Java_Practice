package Chapter_2_DataTypes;

public class IncrementDecrement {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a++); // What will print?
        System.out.println(a); // What will print now?
        System.out.println(++a); // What will print?
        System.out.println(a);
    }
}
